class TrajectoryPolicyNet(nn.Module):
    """Deep policy: [theta_goal/pi, alpha/20deg, phi/20deg] -> trajectory params."""
    def __init__(self, cfg: TrajectoryPolicyConfig):
        if torch is None:
            raise RuntimeError(f"PyTorch import failed: {_TORCH_IMPORT_ERROR}")
        super().__init__()
        h1, h2 = cfg.hidden_sizes
        self.net = nn.Sequential(
            nn.Linear(3, h1), nn.Tanh(),
            nn.Linear(h1, h2), nn.Tanh(),
            nn.Linear(h2, cfg.n_shape + 1),
        )

    def forward(self, x):
        return self.net(x)


def case_to_policy_input(case: CaseSpec) -> np.ndarray:
    max_tilt = math.radians(20.0)
    return np.array([
        case.theta_goal / math.pi,
        case.alpha / max_tilt,
        case.phi / max_tilt,
    ], dtype=np.float32)


def fit_policy_to_local_solutions(
    policy: TrajectoryPolicyNet,
    cases: Sequence[CaseSpec],
    params_list: Sequence[np.ndarray],
    epochs: int = 1500,
    lr: float = 2e-3,
    verbose: bool = True,
):
    """Supervised global-policy fit, as in guided policy search."""
    if torch is None:
        raise RuntimeError(f"PyTorch import failed: {_TORCH_IMPORT_ERROR}")
    x = np.stack([case_to_policy_input(c) for c in cases], axis=0)
    y = np.stack([np.asarray(p, dtype=np.float32) for p in params_list], axis=0)
    x_t = torch.as_tensor(x, dtype=torch.float32)
    y_t = torch.as_tensor(y, dtype=torch.float32)

    opt = optim.Adam(policy.parameters(), lr=lr)
    losses = []
    for ep in range(1, epochs + 1):
        pred = policy(x_t)
        loss = ((pred - y_t) ** 2).mean()
        opt.zero_grad()
        loss.backward()
        opt.step()
        losses.append(float(loss.detach().cpu().item()))
        if verbose and (ep == 1 or ep % 250 == 0 or ep == epochs):
            print(f"policy fit epoch {ep:04d}/{epochs}: mse={losses[-1]:.6g}")
    return losses


def policy_params(policy: TrajectoryPolicyNet, case: CaseSpec) -> np.ndarray:
    x = torch.as_tensor(case_to_policy_input(case), dtype=torch.float32).unsqueeze(0)
    with torch.no_grad():
        p = policy(x).squeeze(0).cpu().numpy()
    p[: -1] = np.clip(p[: -1], -4.0, 4.0)
    p[-1] = np.clip(p[-1], -4.0, 12.0)
    return p.astype(float)



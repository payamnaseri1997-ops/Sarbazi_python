def _maybe_save(fig, save_dir: Optional[str], name: str, show: bool):
    if save_dir is not None:
        os.makedirs(save_dir, exist_ok=True)
        fig.savefig(os.path.join(save_dir, name), dpi=180, bbox_inches="tight")
    if show:
        plt.show()
    else:
        plt.close(fig)


def plot_training_progress(train_output: Dict[str, object], save_dir: Optional[str] = None, show: bool = True):
    losses = np.asarray(train_output["policy_losses"], dtype=float)
    fig = plt.figure(figsize=(8, 4))
    plt.plot(np.arange(1, len(losses) + 1), losses)
    plt.xlabel("Supervised GPS policy-fit epoch")
    plt.ylabel("MSE to local optimized trajectory parameters")
    plt.title("Global trajectory policy fit")
    plt.grid(True, linestyle="--", alpha=0.6)
    _maybe_save(fig, save_dir, "policy_fit_loss.png", show)

    # Local optimizer histories
    fig = plt.figure(figsize=(9, 5))
    for res in train_output["local_results"]:
        hist = res["history"]
        x = [h["iteration"] for h in hist]
        y = [h["best"] for h in hist]
        case_label = f"{res.get('label','')}"
        plt.plot(x, y, alpha=0.8)
    plt.xlabel("CEM iteration")
    plt.ylabel("Best local objective")
    plt.title("Local trajectory optimization progress")
    plt.grid(True, linestyle="--", alpha=0.6)
    _maybe_save(fig, save_dir, "local_cem_progress.png", show)


def plot_case_rollout(
    row: Dict[str, object],
    obj_cfg: TrajectoryConstraintConfig,
    save_dir: Optional[str] = None,
    show: bool = True,
):
    case: CaseSpec = row["case"]
    logs_b = row["existing_logs"]
    logs_r = row["rl_logs"]

    t_b = np.asarray(logs_b["t"])
    t_r = np.asarray(logs_r["t"])

    fig, axes = plt.subplots(5, 1, figsize=(10, 14), sharex=False)
    fig.suptitle(
        f"Existing trajectory vs learned RL/GPS trajectory\n"
        f"goal={case.theta_goal_deg:.1f} deg, alpha={case.alpha_deg:.1f} deg, phi={case.phi_deg:.1f} deg"
    )

    axes[0].plot(t_b, logs_b["theta_ref"], "--", label="existing theta_ref")
    axes[0].plot(t_b, logs_b["theta"], label="existing theta")
    axes[0].plot(t_r, logs_r["theta_ref"], "--", label="RL theta_ref")
    axes[0].plot(t_r, logs_r["theta"], label="RL theta")
    axes[0].axhline(case.theta_goal, linestyle=":", label="theta_goal")
    axes[0].set_ylabel("theta [rad]")
    axes[0].legend(loc="best")
    axes[0].grid(True, linestyle="--", alpha=0.6)

    axes[1].plot(t_b, np.asarray(logs_b["theta"]) - np.asarray(logs_b["theta_ref"]), label="existing theta error")
    axes[1].plot(t_r, np.asarray(logs_r["theta"]) - np.asarray(logs_r["theta_ref"]), label="RL theta error")
    axes[1].set_ylabel("theta error [rad]")
    axes[1].legend(loc="best")
    axes[1].grid(True, linestyle="--", alpha=0.6)

    axes[2].plot(t_b, logs_b["omega"], label="existing omega")
    axes[2].plot(t_b, logs_b["omega_ref"], "--", label="existing omega_ref")
    axes[2].plot(t_r, logs_r["omega"], label="RL omega")
    axes[2].plot(t_r, logs_r["omega_ref"], "--", label="RL omega_ref")
    axes[2].axhline(obj_cfg.omega_limit, linestyle=":", label="+omega limit")
    axes[2].axhline(-obj_cfg.omega_limit, linestyle=":", label="-omega limit")
    axes[2].set_ylabel("omega [rad/s]")
    axes[2].legend(loc="best")
    axes[2].grid(True, linestyle="--", alpha=0.6)

    axes[3].plot(t_b, logs_b["u_total"], label="existing u_total")
    axes[3].plot(t_r, logs_r["u_total"], label="RL u_total")
    axes[3].axhline(obj_cfg.u_command_limit, linestyle=":", label="+command limit")
    axes[3].axhline(-obj_cfg.u_command_limit, linestyle=":", label="-command limit")
    axes[3].set_ylabel("controller command")
    axes[3].legend(loc="best")
    axes[3].grid(True, linestyle="--", alpha=0.6)

    tau_b = np.asarray(logs_b.get("tau_m", np.asarray(logs_b["u_total"])))
    tau_r = np.asarray(logs_r.get("tau_m", np.asarray(logs_r["u_total"])))
    axes[4].plot(t_b, tau_b, label="existing tau_m")
    axes[4].plot(t_r, tau_r, label="RL tau_m")
    axes[4].axhline(obj_cfg.torque_limit, linestyle=":", label="+torque limit")
    axes[4].axhline(-obj_cfg.torque_limit, linestyle=":", label="-torque limit")
    axes[4].set_ylabel("shaft torque [N.m]")
    axes[4].set_xlabel("time [s]")
    axes[4].legend(loc="best")
    axes[4].grid(True, linestyle="--", alpha=0.6)

    fig.tight_layout(rect=[0, 0, 1, 0.96])
    name = f"case_goal_{case.theta_goal_deg:.0f}_tilt_{case.alpha_deg:.0f}_{case.phi_deg:.0f}.png"
    _maybe_save(fig, save_dir, name, show)


def plot_evaluation_summary(
    rows: Sequence[Dict[str, object]],
    save_dir: Optional[str] = None,
    show: bool = True,
):
    labels = [f"{r['case'].theta_goal_deg:.0f}deg\n{r['case'].alpha_deg:.0f}/{r['case'].phi_deg:.0f}deg" for r in rows]
    x = np.arange(len(rows))
    existing_cost = np.array([r["existing_metrics"]["total"] for r in rows], dtype=float)
    rl_cost = np.array([r["rl_metrics"]["total"] for r in rows], dtype=float)

    fig = plt.figure(figsize=(max(10, len(rows) * 0.55), 5))
    width = 0.4
    plt.bar(x - width/2, existing_cost, width, label="existing reference")
    plt.bar(x + width/2, rl_cost, width, label="RL/GPS reference")
    plt.xticks(x, labels, rotation=45, ha="right")
    plt.ylabel("objective cost")
    plt.title("Cost comparison across goals and tilts")
    plt.legend()
    plt.grid(True, axis="y", linestyle="--", alpha=0.6)
    _maybe_save(fig, save_dir, "cost_comparison.png", show)

    fig = plt.figure(figsize=(max(10, len(rows) * 0.55), 5))
    improvement_pct = 100.0 * (existing_cost - rl_cost) / np.maximum(np.abs(existing_cost), 1e-12)
    plt.bar(x, improvement_pct)
    plt.axhline(0.0, linestyle=":")
    plt.xticks(x, labels, rotation=45, ha="right")
    plt.ylabel("improvement [%]")
    plt.title("Positive values mean learned trajectory is better")
    plt.grid(True, axis="y", linestyle="--", alpha=0.6)
    _maybe_save(fig, save_dir, "improvement_percent.png", show)

    # Constraint metrics
    metrics = ["max_abs_omega", "max_abs_tau_m", "energy", "final_theta_error"]
    titles = ["Max |omega|", "Max |shaft torque|", "Command energy", "Final theta error"]
    ylabels = ["rad/s", "N.m", "normalized energy", "rad"]
    for m, title, ylabel in zip(metrics, titles, ylabels):
        fig = plt.figure(figsize=(max(10, len(rows) * 0.55), 5))
        y_b = np.array([abs(r["existing_metrics"][m]) for r in rows], dtype=float)
        y_r = np.array([abs(r["rl_metrics"][m]) for r in rows], dtype=float)
        plt.bar(x - width/2, y_b, width, label="existing")
        plt.bar(x + width/2, y_r, width, label="RL/GPS")
        if m == "max_abs_omega":
            plt.axhline(0.2, linestyle=":", label="omega limit")
        if m == "max_abs_tau_m":
            plt.axhline(2000.0, linestyle=":", label="torque limit")
        plt.xticks(x, labels, rotation=45, ha="right")
        plt.ylabel(ylabel)
        plt.title(title)
        plt.legend()
        plt.grid(True, axis="y", linestyle="--", alpha=0.6)
        _maybe_save(fig, save_dir, f"summary_{m}.png", show)



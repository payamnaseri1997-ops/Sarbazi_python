def make_cases(
    goal_degs: Sequence[float] = (30, 60, 90, 120, 150, 180),
    tilt_degs: Sequence[float] = (0, 10, 20),
    coupled_tilt: bool = True,
) -> List[CaseSpec]:
    """Create cases for theta_goal in [0,180] and tilts 0..20 deg.

    If coupled_tilt=True, cases use alpha=phi=tilt. If False, all alpha/phi
    combinations are generated.
    """
    cases: List[CaseSpec] = []
    for g in goal_degs:
        for a in tilt_degs:
            if coupled_tilt:
                cases.append(CaseSpec(math.radians(g), math.radians(a), math.radians(a)))
            else:
                for p in tilt_degs:
                    cases.append(CaseSpec(math.radians(g), math.radians(a), math.radians(p)))
    return cases


def train_gps_trajectory_policy(
    train_cases: Sequence[CaseSpec],
    traj_cfg: Optional[TrajectoryPolicyConfig] = None,
    obj_cfg: Optional[TrajectoryConstraintConfig] = None,
    cem_iters: int = 12,
    population: int = 32,
    elite_frac: float = 0.20,
    policy_epochs: int = 1500,
    seed: int = 1,
    save_dir: str = "trajectory_rl_results",
) -> Dict[str, object]:
    """Train the GPS-style deep trajectory policy."""
    if traj_cfg is None:
        traj_cfg = TrajectoryPolicyConfig()
    if obj_cfg is None:
        obj_cfg = TrajectoryConstraintConfig()
    os.makedirs(save_dir, exist_ok=True)

    local_results = []
    params_list = []
    for idx, case in enumerate(train_cases):
        result = cem_optimize_case(
            case=case,
            traj_cfg=traj_cfg,
            obj_cfg=obj_cfg,
            n_iter=cem_iters,
            population=population,
            elite_frac=elite_frac,
            seed=seed + 100 * idx,
            verbose=True,
        )
        local_results.append(result)
        params_list.append(result["params"])

    policy = TrajectoryPolicyNet(traj_cfg)
    losses = fit_policy_to_local_solutions(policy, train_cases, params_list, epochs=policy_epochs, lr=2e-3)

    torch.save(policy.state_dict(), os.path.join(save_dir, "trajectory_policy.pt"))
    with open(os.path.join(save_dir, "training_cases.json"), "w") as f:
        json.dump([
            {"theta_goal_deg": c.theta_goal_deg, "alpha_deg": c.alpha_deg, "phi_deg": c.phi_deg}
            for c in train_cases
        ], f, indent=2)

    return dict(
        policy=policy,
        traj_cfg=traj_cfg,
        obj_cfg=obj_cfg,
        local_results=local_results,
        policy_losses=losses,
        train_cases=list(train_cases),
        save_dir=save_dir,
    )


def evaluate_policy_vs_existing(
    policy: TrajectoryPolicyNet,
    cases: Sequence[CaseSpec],
    traj_cfg: TrajectoryPolicyConfig,
    obj_cfg: TrajectoryConstraintConfig,
    seed: int = 123,
) -> List[Dict[str, object]]:
    rows = []
    for i, case in enumerate(cases):
        base_logs, base_theta_ref, base_T, plant_p = simulate_existing_reference(case, seed=seed + i)
        base_metrics = trajectory_objective(base_logs, case.theta_goal, plant_p, obj_cfg)

        p = policy_params(policy, case)
        rl_metrics, rl_logs, rl_theta_ref, rl_T = evaluate_params_for_case(
            case=case,
            params=p,
            traj_cfg=traj_cfg,
            obj_cfg=obj_cfg,
            seed=seed + i,
        )

        rows.append(dict(
            case=case,
            existing_metrics=base_metrics,
            rl_metrics=rl_metrics,
            existing_logs=base_logs,
            rl_logs=rl_logs,
            existing_theta_ref=base_theta_ref,
            rl_theta_ref=rl_theta_ref,
            existing_duration=base_T,
            rl_duration=rl_T,
            rl_params=p,
            improvement=base_metrics["total"] - rl_metrics["total"],
            improvement_ratio=(base_metrics["total"] - rl_metrics["total"]) / max(abs(base_metrics["total"]), 1e-12),
        ))
        print(
            f"Eval goal={case.theta_goal_deg:.1f} deg tilt=({case.alpha_deg:.1f},{case.phi_deg:.1f}) deg: "
            f"existing={base_metrics['total']:.5g}, RL-traj={rl_metrics['total']:.5g}, "
            f"improvement={rows[-1]['improvement_ratio'] * 100:.1f}%"
        )
    return rows



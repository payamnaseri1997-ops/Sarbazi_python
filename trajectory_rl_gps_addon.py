def simulate_reference(
    case: CaseSpec,
    theta_ref: np.ndarray,
    duration: float,
    seed: int = 0,
):
    """Track a supplied reference with the unchanged TDE+SMC controller."""
    plant_p, nom, lqr_w, smc_cfg, cost_cfg = project_params(case.alpha, case.phi)
    plant = OneDOFRotorPlant(plant_p)
    task = Task(theta0=0.0, omega0=0.0, theta_goal=case.theta_goal)

    # rollout_once belongs to your existing project. The reference option only
    # provides theta_ref; it does not change the controller law.
    _, logs = rollout_once(
        plant=plant,
        nom=nom,
        task=task,
        lqr_w=lqr_w,
        smc_cfg=smc_cfg,
        agent=None,
        cost_cfg=cost_cfg,
        reference={"theta": np.asarray(theta_ref, dtype=float), "duration": duration, "kind": "external"},
        seed=seed,
        collect_logs=True,
    )
    return logs, plant_p, nom, lqr_w, smc_cfg, cost_cfg


def simulate_existing_reference(case: CaseSpec, seed: int = 0):
    plant_p, nom, lqr_w, _, _ = project_params(case.alpha, case.phi)
    theta_ref, T = generate_existing_lqr_reference(
        nom=nom,
        plant_p=plant_p,
        lqr_w=lqr_w,
        theta0=0.0,
        theta_goal=case.theta_goal,
        dt=plant_p.dt,
    )
    logs, plant_p, nom, lqr_w, smc_cfg, cost_cfg = simulate_reference(case, theta_ref, T, seed=seed)
    return logs, theta_ref, T, plant_p


def trajectory_objective(
    logs: Dict[str, np.ndarray],
    theta_goal: float,
    plant_p: PlantParams,
    obj_cfg: TrajectoryConstraintConfig,
) -> Dict[str, float]:
    """Compute normalized objective and constraint metrics from rollout logs."""
    t = np.asarray(logs["t"], dtype=float)
    dt = float(plant_p.dt)
    if t.size == 0:
        return {"total": 1e12}

    theta = np.asarray(logs["theta"], dtype=float)
    theta_ref = np.asarray(logs["theta_ref"], dtype=float)
    omega = np.asarray(logs["omega"], dtype=float)
    omega_ref = np.asarray(logs["omega_ref"], dtype=float)
    u_total = np.asarray(logs["u_total"], dtype=float)
    tau_m = np.asarray(logs.get("tau_m", u_total * plant_p.K_t), dtype=float)

    e = theta - theta_ref
    edot = omega - omega_ref

    energy = float(np.sum((u_total / max(obj_cfg.u_command_limit, 1e-12)) ** 2) * dt)
    track = float(np.sum(e ** 2) * dt)
    vel_track = float(np.sum(edot ** 2) * dt)
    final_theta_error = float(theta[-1] - theta_goal)
    final_omega = float(omega[-1])
    final = final_theta_error ** 2 + final_omega ** 2

    ref_vel_violation_arr = np.maximum(np.abs(omega_ref) / obj_cfg.omega_limit - 1.0, 0.0)
    actual_vel_violation_arr = np.maximum(np.abs(omega) / obj_cfg.omega_limit - 1.0, 0.0)
    torque_violation_arr = np.maximum(np.abs(tau_m) / obj_cfg.torque_limit - 1.0, 0.0)
    command_violation_arr = np.maximum(np.abs(u_total) / obj_cfg.u_command_limit - 1.0, 0.0)

    ref_vel_violation = float(np.sum(ref_vel_violation_arr ** 2) * dt)
    actual_vel_violation = float(np.sum(actual_vel_violation_arr ** 2) * dt)
    torque_violation = float(np.sum(torque_violation_arr ** 2) * dt)
    command_violation = float(np.sum(command_violation_arr ** 2) * dt)
    duration = float(t[-1] - t[0]) if len(t) > 1 else 0.0

    total = (
        obj_cfg.w_energy * energy
        + obj_cfg.w_track * track
        + obj_cfg.w_vel_track * vel_track
        + obj_cfg.w_final * final
        + obj_cfg.w_ref_vel_violation * ref_vel_violation
        + obj_cfg.w_actual_vel_violation * actual_vel_violation
        + obj_cfg.w_torque_violation * torque_violation
        + obj_cfg.w_command_violation * command_violation
        + obj_cfg.w_duration * duration
    )

    return dict(
        total=float(total),
        energy=energy,
        track=track,
        vel_track=vel_track,
        final=final,
        final_theta_error=final_theta_error,
        final_omega=final_omega,
        ref_vel_violation=ref_vel_violation,
        actual_vel_violation=actual_vel_violation,
        torque_violation=torque_violation,
        command_violation=command_violation,
        duration=duration,
        max_abs_omega=float(np.max(np.abs(omega))),
        max_abs_omega_ref=float(np.max(np.abs(omega_ref))),
        max_abs_tau_m=float(np.max(np.abs(tau_m))),
        max_abs_u_total=float(np.max(np.abs(u_total))),
    )


def evaluate_params_for_case(
    case: CaseSpec,
    params: np.ndarray,
    traj_cfg: TrajectoryPolicyConfig,
    obj_cfg: TrajectoryConstraintConfig,
    seed: int = 0,
):
    plant_p, _, _, _, _ = project_params(case.alpha, case.phi)
    t, theta_ref, omega_ref, T = build_monotone_reference(
        theta_goal=case.theta_goal,
        params=params,
        dt=plant_p.dt,
        cfg=traj_cfg,
        theta0=0.0,
    )
    logs, plant_p, *_ = simulate_reference(case, theta_ref, T, seed=seed)
    metrics = trajectory_objective(logs, case.theta_goal, plant_p, obj_cfg)
    return metrics, logs, theta_ref, T


# -----------------------------------------------------------------------------
# Local trajectory optimization using CEM
# -----------------------------------------------------------------------------

def cem_optimize_case(
    case: CaseSpec,
    traj_cfg: TrajectoryPolicyConfig,
    obj_cfg: TrajectoryConstraintConfig,
    n_iter: int = 12,
    population: int = 32,
    elite_frac: float = 0.20,
    seed: int = 0,
    verbose: bool = True,
) -> Dict[str, object]:
    """Local CEM optimizer for one goal/tilt case.

    This is the local optimizer in the GPS-style pipeline. It optimizes only
    reference trajectory parameters; the controller/dynamics are unchanged.
    """
    rng = np.random.default_rng(seed)
    dim = traj_cfg.n_shape + 1
    elite_count = max(2, int(round(population * elite_frac)))

    # Start from a symmetric smooth trajectory. Last parameter is duration slack.
    mean = np.zeros(dim, dtype=float)
    std = np.ones(dim, dtype=float)
    std[:traj_cfg.n_shape] = 0.75
    std[-1] = 2.0

    best = dict(cost=np.inf, params=mean.copy(), metrics=None, logs=None, theta_ref=None, duration=None)
    history: List[Dict[str, float]] = []

    # Baseline existing trajectory for comparison.
    base_logs, _, _, plant_p = simulate_existing_reference(case, seed=seed)
    base_metrics = trajectory_objective(base_logs, case.theta_goal, plant_p, obj_cfg)

    for it in range(1, n_iter + 1):
        samples = rng.normal(mean, std, size=(population, dim))
        # Always include current mean and the zero/symmetric trajectory.
        samples[0] = mean
        samples[1] = 0.0
        samples[:, :traj_cfg.n_shape] = np.clip(samples[:, :traj_cfg.n_shape], -4.0, 4.0)
        samples[:, -1] = np.clip(samples[:, -1], -4.0, 12.0)

        costs = np.zeros(population, dtype=float)
        eval_cache = []
        for i, p in enumerate(samples):
            metrics, logs, theta_ref, T = evaluate_params_for_case(case, p, traj_cfg, obj_cfg, seed=seed)
            costs[i] = metrics["total"]
            eval_cache.append((metrics, logs, theta_ref, T))

        order = np.argsort(costs)
        elites = samples[order[:elite_count]]
        elite_costs = costs[order[:elite_count]]

        if costs[order[0]] < best["cost"]:
            metrics, logs, theta_ref, T = eval_cache[order[0]]
            best.update(cost=float(costs[order[0]]), params=samples[order[0]].copy(), metrics=metrics,
                        logs=logs, theta_ref=theta_ref, duration=T)

        # Smooth CEM distribution update.
        new_mean = elites.mean(axis=0)
        new_std = elites.std(axis=0) + 1e-3
        mean = 0.35 * mean + 0.65 * new_mean
        std = 0.35 * std + 0.65 * new_std
        std = np.maximum(std, 0.03)

        history.append(dict(
            iteration=it,
            best=float(best["cost"]),
            elite_mean=float(np.mean(elite_costs)),
            baseline=float(base_metrics["total"]),
        ))
        if verbose:
            print(
                f"CEM case goal={case.theta_goal_deg:.1f} deg, "
                f"tilt=({case.alpha_deg:.1f},{case.phi_deg:.1f}) deg, "
                f"iter={it:02d}, best={best['cost']:.5g}, baseline={base_metrics['total']:.5g}"
            )

    best["history"] = history
    best["baseline_metrics"] = base_metrics
    best["baseline_logs"] = base_logs
    return best



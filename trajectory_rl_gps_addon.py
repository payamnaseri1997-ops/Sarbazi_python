def existing_time_horizon(theta0: float, theta_goal: float) -> float:
    """Same idea as your existing horizon: pi rad move takes 4 seconds."""
    if abs(theta_goal - theta0) < 1e-12:
        return 0.5
    return 4.0 * abs(theta_goal - theta0) / math.pi


def generate_existing_lqr_reference(
    nom: NominalModel,
    plant_p: PlantParams,
    lqr_w: LQRWeights,
    theta0: float,
    theta_goal: float,
    dt: float,
) -> Tuple[np.ndarray, float]:
    """Generate the same style of LQR reference used by the original project.

    This local copy avoids NumPy scalar-conversion errors and lets us pass the
    resulting reference directly to rollout_once(reference={"theta": ...}).
    """
    T = existing_time_horizon(theta0, theta_goal)
    N = max(1, int(round(T / dt)))
    A, B = build_AB(nom, dt)
    Ks = finite_horizon_lqr_gain(A, B, N, lqr_w)
    x_goal = np.array([theta_goal, 0.0], dtype=float)
    x = np.array([theta0, 0.0], dtype=float)

    x_ref = np.zeros((N + 1, 2), dtype=float)
    for k in range(N):
        x_tilde = x - x_goal
        # Same optional speed penalty as your newer implementation.
        if hasattr(lqr_w, "omega_limit_penalty") and hasattr(plant_p, "omega_max"):
            excess = abs(x[1]) - plant_p.omega_max
            if excess > 0:
                x_tilde[1] += lqr_w.omega_limit_penalty * excess * math.copysign(1.0, x[1])
        u = float((-Ks[k] @ x_tilde.reshape(2, 1)).item())
        u = max(-plant_p.u_max, min(plant_p.u_max, u))
        x = A @ x + B.flatten() * u
        x_ref[k + 1, :] = x

    theta_ref = x_ref[:, 0].copy()
    theta_ref[0] = theta0
    return theta_ref, N * dt


# -----------------------------------------------------------------------------
# Monotone reference trajectory parameterization
# -----------------------------------------------------------------------------

def _softplus_np(x: np.ndarray) -> np.ndarray:
    return np.log1p(np.exp(-np.abs(x))) + np.maximum(x, 0.0)


def _shape_basis(z: np.ndarray, n_shape: int) -> np.ndarray:
    """Basis used to shape the normalized velocity profile."""
    cols = []
    for k in range(1, n_shape + 1):
        cols.append(np.sin(k * math.pi * z))
    return np.stack(cols, axis=1)


def build_monotone_reference(
    theta_goal: float,
    params: np.ndarray,
    dt: float,
    cfg: TrajectoryPolicyConfig,
    theta0: float = 0.0,
) -> Tuple[np.ndarray, np.ndarray, np.ndarray, float]:
    """Build a smooth monotone reference from trajectory parameters.

    The parameters define a positive normalized velocity shape v(z). The
    cumulative integral gives h(z) from 0 to 1. Duration is chosen so that
    |theta_ref_dot| is approximately bounded by cfg.omega_ref_limit.

    Returns
    -------
    t : ndarray
    theta_ref : ndarray
    omega_ref : ndarray
    duration : float
    """
    params = np.asarray(params, dtype=float).reshape(-1)
    n_shape = cfg.n_shape
    if params.size != n_shape + 1:
        raise ValueError(f"params must have length {n_shape + 1}, got {params.size}")

    delta = float(theta_goal - theta0)
    if abs(delta) < 1e-12:
        t = np.arange(0.0, cfg.min_duration + 0.5 * dt, dt)
        theta_ref = np.full_like(t, theta0, dtype=float)
        omega_ref = np.zeros_like(t)
        return t, theta_ref, omega_ref, float(t[-1])

    # High-resolution normalized grid for shape construction.
    z_grid = np.linspace(0.0, 1.0, 1001)
    B = _shape_basis(z_grid, n_shape)
    logits = B @ params[:n_shape]

    # Endpoint factor makes reference velocity near zero at the beginning/end.
    endpoint_factor = z_grid * (1.0 - z_grid)
    v_shape = endpoint_factor * np.exp(np.clip(logits, -5.0, 5.0)) + 1e-6

    # Normalize integral to 1.
    dz = z_grid[1] - z_grid[0]
    area = float(np.sum((v_shape[:-1] + v_shape[1:]) * 0.5 * np.diff(z_grid)))
    v_norm = v_shape / max(area, 1e-12)   # h'(z)
    h_grid = np.zeros_like(z_grid)
    h_grid[1:] = np.cumsum(0.5 * (v_norm[1:] + v_norm[:-1]) * dz)
    h_grid /= max(h_grid[-1], 1e-12)

    max_hprime = float(np.max(np.abs(v_norm)))
    T_min_vel = abs(delta) * max_hprime / max(cfg.omega_ref_limit, 1e-12)
    duration_slack = float(_softplus_np(np.array([params[-1]]))[0])
    T = T_min_vel + duration_slack
    T = float(np.clip(T, cfg.min_duration, cfg.max_duration))

    N = max(1, int(round(T / dt)))
    t = np.arange(N + 1, dtype=float) * dt
    T = float(t[-1])
    z = np.clip(t / max(T, 1e-12), 0.0, 1.0)
    h = np.interp(z, z_grid, h_grid)
    theta_ref = theta0 + delta * h
    theta_ref[0] = theta0
    theta_ref[-1] = theta_goal
    omega_ref = finite_diff(theta_ref, dt)
    return t, theta_ref, omega_ref, T



def run_full_trajectory_rl_experiment(
    train_goal_degs: Sequence[float] = (30, 60, 90, 120, 150, 180),
    test_goal_degs: Sequence[float] = (15, 45, 75, 105, 135, 165, 180),
    tilt_degs: Sequence[float] = (0, 10, 20),
    cem_iters: int = 8,
    population: int = 24,
    policy_epochs: int = 1000,
    save_dir: str = "trajectory_rl_results",
    show_plots: bool = True,
):
    """Run training, testing, and plotting.

    Start with the default values above to smoke-test. Increase cem_iters,
    population, and policy_epochs for stronger results.
    """
    traj_cfg = TrajectoryPolicyConfig(
        n_shape=5,
        omega_ref_limit=0.2,
        min_duration=0.5,
        max_duration=80.0,
        hidden_sizes=(64, 64),
    )
    obj_cfg = TrajectoryConstraintConfig(
        omega_limit=0.2,
        torque_limit=2000.0,
        u_command_limit=500.0,
    )

    train_cases = make_cases(train_goal_degs, tilt_degs, coupled_tilt=True)
    train_output = train_gps_trajectory_policy(
        train_cases=train_cases,
        traj_cfg=traj_cfg,
        obj_cfg=obj_cfg,
        cem_iters=cem_iters,
        population=population,
        elite_frac=0.20,
        policy_epochs=policy_epochs,
        seed=1,
        save_dir=save_dir,
    )

    test_cases = make_cases(test_goal_degs, tilt_degs, coupled_tilt=True)
    rows = evaluate_policy_vs_existing(train_output["policy"], test_cases, traj_cfg, obj_cfg, seed=1000)

    plot_training_progress(train_output, save_dir=save_dir, show=show_plots)
    plot_evaluation_summary(rows, save_dir=save_dir, show=show_plots)

    # Plot a few representative cases: low/high tilt and mid/high goal.
    for row in rows:
        g = round(row["case"].theta_goal_deg)
        a = round(row["case"].alpha_deg)
        if (g in (75, 105, 180)) and (a in (0, 20)):
            plot_case_rollout(row, obj_cfg, save_dir=save_dir, show=show_plots)

    # Save numeric table.
    table = []
    for r in rows:
        c = r["case"]
        table.append(dict(
            theta_goal_deg=c.theta_goal_deg,
            alpha_deg=c.alpha_deg,
            phi_deg=c.phi_deg,
            existing_cost=r["existing_metrics"]["total"],
            rl_cost=r["rl_metrics"]["total"],
            improvement_ratio=r["improvement_ratio"],
            existing_energy=r["existing_metrics"]["energy"],
            rl_energy=r["rl_metrics"]["energy"],
            existing_max_omega=r["existing_metrics"]["max_abs_omega"],
            rl_max_omega=r["rl_metrics"]["max_abs_omega"],
            existing_max_tau_m=r["existing_metrics"]["max_abs_tau_m"],
            rl_max_tau_m=r["rl_metrics"]["max_abs_tau_m"],
            existing_final_error=r["existing_metrics"]["final_theta_error"],
            rl_final_error=r["rl_metrics"]["final_theta_error"],
        ))
    with open(os.path.join(save_dir, "evaluation_summary.json"), "w") as f:
        json.dump(table, f, indent=2)

    print("\n=== Summary ===")
    n_better = sum(1 for r in rows if r["rl_metrics"]["total"] < r["existing_metrics"]["total"])
    print(f"RL/GPS trajectory better in {n_better}/{len(rows)} test cases.")
    print(f"Results saved in: {os.path.abspath(save_dir)}")

    return dict(train_output=train_output, evaluation_rows=rows, traj_cfg=traj_cfg, obj_cfg=obj_cfg)



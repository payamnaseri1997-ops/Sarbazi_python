# -*- coding: utf-8 -*-
"""
trajectory_rl_gps_addon.py

Add-on for LQR_TrjOPt_TDESMCwithRLresidual.py.

Purpose
-------
Keeps your existing plant dynamics and TDE+SMC controller unchanged, but adds
an RL/GPS-style trajectory optimizer that learns a goal-conditioned reference
trajectory generator.

Two references are compared:
    1) Existing finite-horizon LQR/iLQR-like reference from your current setup.
    2) Learned GPS-style trajectory reference generated by a deep policy.

The controller remains your original TDE+SMC tracker. The RL/GPS part changes
only the reference trajectory theta_ref(t), not the controller law.

How it works
------------
For each training case (theta_goal, alpha tilt, phi tilt):
    - A local black-box trajectory optimizer (CEM) finds trajectory parameters.
    - A neural policy learns the map:
          [theta_goal, alpha, phi] -> trajectory parameters
      This is a small Guided Policy Search style procedure:
          local trajectory optimization -> global goal-conditioned policy.

The trajectory parameterization is monotone and time-scaled so that the
reference angular velocity is constrained to approximately |theta_ref_dot| <= 0.2 rad/s.
The evaluation objective also penalizes actual velocity and torque violations.

Requirements
------------
This file expects the following module to be next to it / on PYTHONPATH:
    LQR_TrjOPt_TDESMCwithRLresidual.py

Run from the same folder as your project, or adjust the import below.
"""


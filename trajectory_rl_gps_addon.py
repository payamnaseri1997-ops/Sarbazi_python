@dataclass
class TrajectoryConstraintConfig:
    """Constraints and objective weights for trajectory optimization."""
    omega_limit: float = 0.2          # [rad/s] max allowed actual/reference angular velocity
    torque_limit: float = 2000.0      # [N.m] max allowed actual shaft torque tau_m
    u_command_limit: float = 500.0    # [command units or N.m reference] plant command limit

    # Normalized objective weights. Constraint penalties should dominate.
    w_energy: float = 1.0             # integral (u_total/u_max)^2 dt
    w_track: float = 10.0             # integral theta tracking error^2 dt
    w_vel_track: float = 2.0          # integral velocity tracking error^2 dt
    w_final: float = 500.0            # final theta and omega error
    w_ref_vel_violation: float = 1.0e4
    w_actual_vel_violation: float = 1.0e4
    w_torque_violation: float = 1.0e4
    w_command_violation: float = 1.0e4
    w_duration: float = 0.0           # optional; leave 0 if duration is not part of objective


@dataclass
class TrajectoryPolicyConfig:
    """Configuration for the trajectory parameterization and policy."""
    n_shape: int = 5
    omega_ref_limit: float = 0.2
    min_duration: float = 0.5
    max_duration: float = 80.0
    hidden_sizes: Tuple[int, int] = (64, 64)


@dataclass
class CaseSpec:
    """One trajectory optimization/evaluation case."""
    theta_goal: float
    alpha: float
    phi: float

    @property
    def theta_goal_deg(self) -> float:
        return math.degrees(self.theta_goal)

    @property
    def alpha_deg(self) -> float:
        return math.degrees(self.alpha)

    @property
    def phi_deg(self) -> float:
        return math.degrees(self.phi)


